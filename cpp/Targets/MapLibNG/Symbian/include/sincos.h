/* methods for accessing the tables */
static inline float SIN(float angle)
{
	/* round-off the angle */
	int iAngle = (int)angle;

	// Increase angle if it is less than zero.
   while ( iAngle < 0 ) {
      iAngle += 360;
   }
   // Remove part larger than 360
   iAngle %= 360;

   return SINTABLE[iAngle];
	
}

static inline float COS(float angle)
{
   return SIN( angle + 90 );
}


